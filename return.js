function sum (n1,n2){
    const total= n1+n2;
    return total
}
function sub (n1,n2){
    const total= n1-n2;
    return total
}
function mul (n1,n2){
    const total= n1*n2;
    return total
}
function dev (n1,n2){
    const total= n1/n2;
    return total
}

console.log(sum(1,2))

console.log(sub(1,2))

console.log(mul(1,2))

console.log(dev(1,2))