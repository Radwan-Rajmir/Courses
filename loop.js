function forloop(){
    const lop= [1,2,4,"odell","tech", 1.3,4,4,3]
    for(i=0;i<=lop.length-1;i++){
        let data=lop[i]
        console.log(data)
    }
}

forloop()
function whileloop(){
    const lop= [1,2,4,"odell","tech", 1.3,4,4,3]
    let i=0
    while(i<=lop.length-1){
        let data=lop[i]
        console.log(data)
        i++  
    }

}
